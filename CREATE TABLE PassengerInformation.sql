CREATE TABLE PassengerInformation
(
    PASSENGERID INT PRIMARY KEY,
    PASSENGERNAME VARCHAR(50),
    AGE NUMBER(3),
    EMAIL VARCHAR(50),
    PASSWORD VARCHAR(30),
    ADDRESS VARCHAR(100),
    PHONE NUMBER(10),
    PNRNO NUMBER NOT NULL, 
    TRAVELDATE DATE NOT NULL,
    SOURCE VARCHAR2(20)NOT NULL,
    DESTINATION VARCHAR2(20) NOT NULL,
    SEATPREFERENCE VARCHAR2(20) NOT NULL,
    MEALPREFERENCE VARCHAR2(20) NOT NULL
);

